/*Escreva um programa que defina três números em constantes e retorne true se pelo menos uma das três for par. Caso contrário, ele retorna false .
Bonus: use somente um if .*/

const a = 6;
const b = 3;
const c = 9;

if ((a / a) === 1 || (b / b) === 1 || (c / c) === 1) {
console.log('true');
} else {
    console.log('false');
}